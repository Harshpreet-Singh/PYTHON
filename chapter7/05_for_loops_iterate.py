# /\/\/\/\/\/\/\/\/\/\/\/\/\/\-------ITERATING LIST-------/\/\/\/\/\/\/\/\/\/\/\/\/\/\
l = [1, 2, 45, 456, 342, 78]
for i in l:
    print(i)
print("\n")

# /\/\/\/\/\/\/\/\/\/\/\/\/\/\-------ITERATING TUPLE-------/\/\/\/\/\/\/\/\/\/\/\/\/\/\
t = (6, 256, 24, 895)
for i in t:
    print(i)
print("\n")

# /\/\/\/\/\/\/\/\/\/\/\/\/\/\-------ITERATING STRING-------/\/\/\/\/\/\/\/\/\/\/\/\/\/\
